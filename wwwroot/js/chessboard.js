function hover()
{
    var one = document.getElementById("one");  
    one.style.color = "rgba(255,255,255,0)";
    one.style.backgroundColor = "rgba(255,255,255,0.3)";
    //one.style.color = "red";
}
function reset()
{
    var one = document.getElementById("one");  
    one.style.color = "rgba(255,255,255,0)";
    one.style.backgroundColor = "rgba(255, 255, 255, 0)";
    one.style.color= "rgba(0, 0, 0, 1)";
}
